package Classe;

/**
 *
 * @author MARCELLINA
 */
public class ClasseCaissier {
    private String cinCaissier;
    private String nomCaissier;
    private String prenCaissier;
    private String fonctCaissier;
    private String telCaissier;

    public ClasseCaissier(String cinCaissier, String nomCaissier, String prenCaissier, String fonctCaissier, String telCaissier) {
        this.cinCaissier = cinCaissier;
        this.nomCaissier = nomCaissier;
        this.prenCaissier = prenCaissier;
        this.fonctCaissier = fonctCaissier;
        this.telCaissier = telCaissier;
    }

    public String getCinCaissier() {
        return cinCaissier;
    }

    public void setCinCaissier(String cinCaissier) {
        this.cinCaissier = cinCaissier;
    }


    public String getNomCaissier() {
        return nomCaissier;
    }

    public void setNomCaissier(String nomCaissier) {
        this.nomCaissier = nomCaissier;
    }

    public String getPrenCaissier() {
        return prenCaissier;
    }

    public void setPrenCaissier(String prenCaissier) {
        this.prenCaissier = prenCaissier;
    }

    public String getFonctCaissier() {
        return fonctCaissier;
    }

    public void setFonctCaissier(String fonctCaissier) {
        this.fonctCaissier = fonctCaissier;
    }

    public String getTelCaissier() {
        return telCaissier;
    }

    public void setTelCaissier(String telCaissier) {
        this.telCaissier = telCaissier;
    }
  
    
    
    
    
    
    
            
}
