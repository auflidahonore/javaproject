    private void tftitactKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tftitactKeyTyped
        // TODO add your handling code here:
        labtit.setText("Caractère invalide");
        String valeur = tftitact.getText().toString();
        int ln = valeur.length();
        if (tftitact.getText().matches("[A-Z,a-z,é,à,è,ù,ç,',â, ]*")) {
            tftitact.setText(valeur);
            labtit.setVisible(false);
        } else {
            valeur = valeur.substring(0, ln - 1);
            tftitact.setText(valeur);
            labtit.setVisible(true);
            int val = 0;
        }
    }//GEN-LAST:event_tftitactKeyTyped

