   private void Recherche1() {                                           
        String rech=tfrecherche.getText();
        String num = tfnumcom.getText();
        String nom = tfnomcli.getText();
        String pren = tfprencli.getText();
        String datecm = ((JTextField)datecom.getDateEditor().getUiComponent()).getText();
        String nomact = cbnomact.getSelectedItem().toString();
        String tit = tftitact.getText();
        String typeact = cbtypeact.getSelectedItem().toString();
        String dateat = ((JTextField)dateact.getDateEditor().getUiComponent()).getText();
        String datefact = ((JTextField)datefinact.getDateEditor().getUiComponent()).getText();
        String dur = tfdureact.getText();
        String prix = tfprixcom.getText();
        try{
            dt.setRowCount(0);
            Rs=St.executeQuery("SELECT * FROM commander WHERE (NOM_CLI LIKE '%"+rech+"%' OR PREN_CLI LIKE '%"+rech+"%' OR TITRE_ACT LIKE '%"+rech+"%' OR NUM_COM LIKE '%"+rech+"%' OR PRIX_COM LIKE '%"+rech+"%' OR DURE_ACT LIKE '%"+rech+"%')");
            i=0;
            while(Rs.next()){
                num = Rs.getString("NUM_COM");
                nom = Rs.getString("NOM_CLI");
                pren = Rs.getString("PREN_CLI");
                datecm = Rs.getString("DATE_COM");
                nomact = Rs.getString("NOM_ACT");
                tit = Rs.getString("TITRE_ACT");
                typeact = Rs.getString("TYPE_ACT");
                dateat = Rs.getString("DATE_ACT");
                datefact = Rs.getString("DATE_FIN_ACT");
                dur = Rs.getString("DURE_ACT");
                prix = Rs.getString("PRIX_COM");
                
                               
                Object[] dep = {num, nom, pren, datecm, nomact, tit, typeact, dateat, datefact, dur, prix} ;
	        dt.addRow(dep);
            }
            i=i+1;
            if(dt.getRowCount()==0){
                JOptionPane.showMessageDialog(null, " Aucun resultat trouver!");
                // AfficheDisp("");
            }else{
                i=0;
                
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Erreur de rechercher\n"+e);
        }
    }
   
